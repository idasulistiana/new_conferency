					<p>
						<p align="justify">&emsp;Biomass is an organic matter originating from agricultural products, plantation products, grasses, industrial waste, urban garbage, household waste, bioaquatic, animal waste, etc.  Biomass can be utilized in various industrial applications.  However, today, the term biomass is mostly used to refer to an energy (fuel) source.  Some of the advantages of energy resulted from biomass is that it is renewable, environmentally friendly, produces less emission, and ensures energy availability.  </p>
						<p align="justify">&emsp;In addition to its use as an energy source, biomass can be further processed into various types of chemicals needed in chemical, pharmaceutical, oil, and gas, feed, and agricultural industries.  One of the biggest challenges in biomass utilization is the production of high quality commercial products which is energy saving, less waste producing, and sustainable. </p>
						<p align="justify">&emsp;Each country has different potential biomass feedstock.  For example, oil palm biomass is plenty in Indonesia and Malaysia, cassava and sugarcane biomass in Thailand, rice biomass in Vietnam, sugarcane biomass in Brazil, wood biomass in Canada.  This different biomass feedstock has stimulated the development of biomass production technology.  This notion has been the background of this International Conference on Biomass: Technology, Application, and Sustainable Development which is conducted as a sharing medium of ideas and knowledge to accelerate the advancement of technology in biomass processing.  This technology is important to create variety of products which can be produced with high added value in a cheaper and environmentally friendly process so that they can be commercialized.  </p>

